print("El resultado de la suma de los 2 numeros es:", suma)

resta = numero1 - numero2 
print("El resultado de la resta de los 2 numeros es:", resta)

multipli = numero1 * numero2
print("El resultado del a multiplicacino de los 2 numeros es:", multipli)

division = numero1/numero2
print("El resultado de la division de los 2 numros es:", division)